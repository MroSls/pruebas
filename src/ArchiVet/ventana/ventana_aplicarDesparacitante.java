    void CargarTablaDesparacitar() {
        DefaultTableModel mode = (DefaultTableModel) TDesparacitacion.getModel();
        mode.setRowCount(0);
        obd.tablaDesparapli(TDesparacitacion, mode, Mascota.getText());
    }

    public void verificar() {
        if (!(Lote.getText().isEmpty() || Precio.getText().isEmpty())) {
            JOptionPane.showMessageDialog(null, "Se ha guardado con exito", "Guardado", JOptionPane.DEFAULT_OPTION);
            long fec = Aplicacion.getDate().getTime();
            java.sql.Date fech = new java.sql.Date(fec);
            obd.aplidesp(fech, Descripcion.getSelectedItem().toString(), Lote.getText(), Double.valueOf(Precio.getText()), Mascota.getText(), usuario.getUsuario());
            obd.historial(8, Mascota.getText(), "Macho", "Desparacitacion", Float.parseFloat(Precio.getText()), Descripcion.getSelectedItem().toString(), 2);
            Limpiar();
            CargarTablaDesparacitar();
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(null, "No se ha guardado\nVerifique los campos", "Error", JOptionPane.DEFAULT_OPTION);
        }
    }

